// No direct access to this file
defined('_JEXEC') or die('Restricted access');

// import Joomla view library
jimport('joomla.application.component.view');

/**
 * Bigbluebutton View class
 */
class BigbluebuttonViewBigbluebutton extends JViewLegacy
{
	/**
	 * View display method
	 * @return void
	 */
	function display($tpl = null)
	{
		// Assign data to the view
		$this->icons			= $this->get('Icons');
		$this->contributors		= BigbluebuttonHelper::getContributors();
		
		// get the manifest details of the component
		$this->manifest = BigbluebuttonHelper::manifest();
		
		// Set the toolbar
		$this->addToolBar();
		
		// Check for errors.
		if (count($errors = $this->get('Errors')))
		{
			throw new Exception(implode("\n", $errors), 500);
		}

		// Display the template
		parent::display($tpl);

		// Set the document
		$this->setDocument();
	}

	/**
	 * Setting the toolbar
	 */
	protected function addToolBar()
	{
		$canDo = BigbluebuttonHelper::getActions('bigbluebutton');
		JToolBarHelper::title(JText::_('COM_BIGBLUEBUTTON_DASHBOARD'), 'grid-2');

		// set help url for this view if found
		$help_url = BigbluebuttonHelper::getHelpUrl('bigbluebutton');
		if (BigbluebuttonHelper::checkString($help_url))
		{
			JToolbarHelper::help('COM_BIGBLUEBUTTON_HELP_MANAGER', false, $help_url);
		}

		if ($canDo->get('core.admin') || $canDo->get('core.options'))
		{
			JToolBarHelper::preferences('com_bigbluebutton');
		}
	}

	/**
	 * Method to set up the document properties
	 *
	 * @return void
	 */
	protected function setDocument()
	{
		$document = JFactory::getDocument();
		
		// add dashboard style sheets
		$document->addStyleSheet(JURI::root() . "administrator/components/com_bigbluebutton/assets/css/dashboard.css");
		
		// set page title
		$document->setTitle(JText::_('COM_BIGBLUEBUTTON_DASHBOARD'));
		
		// add manifest to page JavaScript
		$document->addScriptDeclaration("var manifest = jQuery.parseJSON('" . json_encode($this->manifest) . "');", "text/javascript");
	}
}
